// ============================================================
//  Структура программного таймера-одновыстрела
// ============================================================
struct TimerOnce {
  uint32_t start_ms = 0;
  uint32_t period = 0;
  bool running = false;

  void begin(uint32_t prd) {
    period = prd;
    running = false;
  }

  void startTimer() {
    start_ms = millis();
    running = true;
  }

  void stopTimer() {
    running = false;
  }

  // Возвращает true ОДИН РАЗ при срабатывании
  bool tick() {
    if (!running) return false;
    if (millis() - start_ms >= period) {
      running = false;
      return true;
    }
    return false;
  }

  bool active() {
    return running;
  }
};


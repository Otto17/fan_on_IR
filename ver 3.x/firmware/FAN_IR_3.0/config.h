#pragma once

// ============================================================
//  Пины реле
// ============================================================
#define RELAY1 4  // Режим 1 — Низкая скорость
#define RELAY2 7  // Режим 2 — Средняя скорость
#define RELAY3 8  // Режим 3 — Высокая скорость
#define RELAY4 3  // Режим 4 — Пониженная скорость через конденсатор

// Тип реле: LOW = включается минусом, HIGH = включается плюсом
#define RELAY_TYPE LOW
// Задержка (мс) размыкания контактов реле перед переключением
#define DELAY_REL_OFF 40

// ============================================================
//  Пины LED индикации
// ============================================================
#define LED_GREAN 10  // Зелёный — режим 1
#define LED_BLUE 11   // Синий   — режим 2
#define LED_RED 12    // Красный — режим 3

// ============================================================
//  Пины периферии
// ============================================================
#define INTERRUPT_PIN_IR 2  // ИК приёмник (вход прерывания)
#define SERVOMOTOR 9        // Сервопривод

// ============================================================
//  WatchDog
//  ATmega8: ~2.2 сек, ATmega48: ~2 сек
// ============================================================
#define WDT_ENABLE 1  // 1 — включить, 0 — выключить
#define WTD_CPU 0     // 1 — ATmega8,  0 — ATmega48

// ============================================================
//  EEPROM кольцевой буфер
//  ATmega48: 256 байт → пишем 128
//  ATmega8:  512 байт → пишем 254
// ============================================================
#define EEPROM_MARKER 128   // Маркер (половина объёма EEPROM, макс. 254)
#define DELAY_EE_TIME 7000  // Задержка (мс) отложенной записи положения серво

// ============================================================
//  Таймеры автовыключения вентилятора (мс)
// ============================================================
#define TIMER_MODE1 5400000UL      // 1 час 30 мин (таймер 1)
#define TIMER_MODE2 14400000000UL  // 4 часа (таймер 2)

// ============================================================
//  Сервопривод — углы поворота
// ============================================================
#define RIGHT_CORNER 140          // Максимальный угол вправо (градусы)
#define LEFT_CORNER 10            // Максимальный угол влево  (градусы)
#define AUTO_DELAY_TIME_SERVO 40  // Интервал (мс) между шагами автоповоротов
#define AUTO_DIR_ITERATION 1      // Шаг автоповорота (градусы)

#define MANUAL_DELAY_TIME_SERVO 60  // Интервал (мс) между шагами — для ручных поворотов
#define COUNT_DIR_ITERATION 2       // Шаг ручного поворота (градусы)

// Параметры ШИМ сервопривода
#define SERVO_MIN_US 500   // Длительность импульса (мкс) для   0°
#define SERVO_MAX_US 2400  // Длительность импульса (мкс) для 180°
#define SERVO_PERIOD 20    // Период повторения импульса (мс) — 50 Гц

// ============================================================
//  Коды кнопок ИК пульта (8 бит, протокол NEC)
// ============================================================
#define IR_OFF 0x48  // OFF:        Выключить всё
#define IR_OK 0x84   // OK:         Вкл/Выкл LED индикацию

#define IR_UP 0x01     // ↑:          Автоповороты Вкл.
#define IR_DOWN 0x81   // ↓:          Автоповороты Выкл.
#define IR_LEFT 0x8A   // ←:          Ручной поворот влево
#define IR_RIGHT 0xB2  // →:          Ручной поворот вправо

#define IR_1 0x80           // 1:          Режим 1 + Таймер 1
#define IR_4 0x20           // 4:          Режим 1 + Таймер 2
#define IR_2 0x40           // 2:          Режим 2 + Таймер 1
#define IR_5 0xA0           // 5:          Режим 2 + Таймер 2
#define IR_3 0xC0           // 3:          Режим 3 + Таймер 1
#define IR_6 0x60           // 6:          Режим 3 + Таймер 2
#define IR_PLAY_PAUSE 0xA8  // PLAY/PAUSE: Режим 4 + Таймер 1
#define IR_STOP 0x28        // STOP:       Режим 4 + Таймер 2